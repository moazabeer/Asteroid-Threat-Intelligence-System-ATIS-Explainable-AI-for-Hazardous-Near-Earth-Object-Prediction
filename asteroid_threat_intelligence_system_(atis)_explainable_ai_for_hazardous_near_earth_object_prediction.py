def expected_calibration_error(
    y_true,
    y_prob,
    bins=15
):

    bin_boundaries = np.linspace(
        0,
        1,
        bins + 1
    )

    ece = 0

    for i in range(bins):

        mask = (
            (y_prob >= bin_boundaries[i]) &
            (y_prob < bin_boundaries[i+1])
        )

        if np.sum(mask) > 0:

            accuracy = np.mean(
                y_true[mask]
            )

            confidence = np.mean(
                y_prob[mask]
            )

            ece += (
                np.abs(
                    accuracy -
                    confidence
                )
                *
                np.sum(mask)
                /
                len(y_true)
            )

    return ece

