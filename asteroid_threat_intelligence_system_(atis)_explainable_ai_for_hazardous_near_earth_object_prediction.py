def physics_loss(

    y_true,

    y_pred,

    semi_major_axis,

    period
):

    prediction_loss = tf.reduce_mean(

        tf.square(
            y_true -
            y_pred
        )
    )

    kepler_loss = tf.reduce_mean(

        tf.square(

            period**2 -

            semi_major_axis**3
        )
    )

    return (

        prediction_loss +

        0.01 * kepler_loss
    )

